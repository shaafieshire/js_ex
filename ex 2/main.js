let a = 10;
let b = "10";

console.log(a == b); // true without type coercion
console.log(a === b); // false with type coercion
console.log(a != b); // false without type coercion
console.log(a !== b); // true with type coercion
console.log(a > b); // false without type coercion
console.log(a < b); // false without type coercion
console.log(a >= b); // true without type coercion
console.log(a <= b); // true without type coercion